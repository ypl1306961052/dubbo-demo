    public static String getUniqueFileName() {
        String str = UUID.randomUUID().toString();
        return str.replace("-", "");
    }

    public static String getFileName(String file) {
        String[] split = fileSplit(file.substring(0, file.lastIndexOf(".")));
        return getLastValue(split);
    }

    public static String getLastValue(String[] split) {
        return split.length >= 2 ? split[split.length - 1] : split[0];
    }

    public static String getOriginalName(String file) {
        String[] split = fileSplit(file);
        return getLastValue(split);
    }

    public static String[] fileSplit(String file) {
        return file.split("/");
    }
